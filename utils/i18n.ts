function getByPath(obj: unknown, path: string): string | undefined {
  const value = path.split(".").reduce<unknown>((acc, key) => {
    if (typeof acc !== "object" || acc === null) return undefined
    return (acc as Record<string, unknown>)[key]
  }, obj)
  return typeof value === "string" ? value : undefined
}

export function t(lang: AppLanguage, key: string): string {
  return getByPath(messages[lang], key) ?? getByPath(messages.ko, key) ?? key
}

export function useI18n() {
  const appSettings = useOptionalAppSettings()
  const appLanguage = appSettings?.appLanguage ?? "ko"
  const translate = useMemo(() => {
    return (key: string) => t(appLanguage, key)
  }, [appLanguage])

  return useMemo(
    () => ({
      t: translate,
    }),
    [translate],
  )
}
